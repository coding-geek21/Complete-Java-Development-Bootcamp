public class Decimals {
    public static void main(String[] args) {
        double price = 66000.45;

        System.out.println("The price of the car which i bought is Rs: " + price);

        double dividend = 25;
        double divisor = 2;

        System.out.println(dividend + " divided by " + divisor + " = " + dividend / divisor);
    }
}
